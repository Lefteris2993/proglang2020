public class Node {
    private int i;
    private int j;
    private int t;

    public Node(int i, int j, int t) {
        this.i = i;
        this.j = j;
        this.t = t;
    }

    public int getI() {
        return i;
    }

    public int getJ() {
        return j;
    }

    public int getT() {
        return t;
    }

}
